//program to find the size of each data types
#include <stdio.h>
int main(){
    int a;
    float b;
    char c;
    double d;
    printf("The size of Integer Type is %d\n",sizeof(a));
    printf("The size of Float Type is %d\n",sizeof(b));
    printf("The size of Char Type is %d\n",sizeof(c));
    printf("The size of Double Type is %d\n",sizeof(d));
    return 0;
}